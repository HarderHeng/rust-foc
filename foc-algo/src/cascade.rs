#[cfg(test)]
mod tests {
    use super::*;
    use crate::transforms::LibmTrig;

    fn make_vdc(mode: Mode) -> FocController {
        let mut c = FocController::new(mode);
        c.meas.vdc = 24.0;
        c
    }

    #[test]
    fn off_mode_zero_duty() {
        let mut c = FocController::new(Mode::Off);
        c.meas.vdc = 24.0;
        c.update::<LibmTrig>(0.0001);
        approx(c.current.duty.ta, 0.0);
        approx(c.current.duty.tb, 0.0);
        approx(c.current.duty.tc, 0.0);
        approx(c.runtime.iq_target, 0.0);
    }

    #[test]
    fn default_mode_is_off() {
        let c = FocController::default();
        assert_eq!(c.mode, Mode::Off);
    }

    #[test]
    fn torque_mode_passes_iq_directly() {
        let mut c = make_vdc(Mode::Torque);
        c.target.iq = 1.5;
        c.update::<LibmTrig>(0.0001);
        // No PI running (default kp=0), so current loop's vd=vq=0,
        // SVPWM centred, duty 0.5.
        approx(c.current.duty.ta, 0.5);
        approx(c.runtime.iq_target, 1.5);
    }

    #[test]
    fn speed_mode_runs_speed_loop() {
        let mut c = make_vdc(Mode::Speed);
        c.speed.pid.kp = 1.0;
        c.target.speed_ref = 2.0;
        c.meas.speed = 0.0;
        c.update::<LibmTrig>(0.0001);
        // iq_target = 1.0 × 2.0 = 2.0
        approx(c.runtime.iq_target, 2.0);
    }

    #[test]
    fn position_mode_runs_both_loops() {
        let mut c = make_vdc(Mode::Position);
        c.position.kp = 1.0;
        c.speed.pid.kp = 1.0;
        c.target.position = 1.0;     // 1 rad away
        c.meas.position = 0.0;
        c.meas.speed = 0.0;
        c.update::<LibmTrig>(0.0001);
        // Position: kp_p × 1.0 = 1.0 → ω_ref = 1.0
        // Speed: kp_s × 1.0 = 1.0 → iq_target = 1.0
        approx(c.runtime.speed_target, 1.0);
        approx(c.runtime.iq_target, 1.0);
    }

    #[test]
    fn mode_can_be_switched() {
        let mut c = make_vdc(Mode::Speed);
        c.speed.pid.kp = 1.0;
        c.target.speed_ref = 2.0;
        c.meas.speed = 0.0;
        c.update::<LibmTrig>(0.0001);
        let iq_speed = c.runtime.iq_target;
        approx(iq_speed, 2.0);

        c.mode = Mode::Off;
        c.update::<LibmTrig>(0.0001);
        approx(c.runtime.iq_target, 0.0);
        approx(c.current.duty.ta, 0.0);
    }

    #[test]
    fn feedforward_propagates_through_speed_mode() {
        let mut c = make_vdc(Mode::Speed);
        c.speed.pid.kp = 0.0;
        c.speed.feedforward.inertia_gain = 0.5;
        c.meas.accel = 10.0;
        c.update::<LibmTrig>(0.0001);
        approx(c.runtime.iq_target, 5.0);
    }

    fn approx(a: f32, b: f32) {
        assert!((a - b).abs() < 1e-5, "expected {b}, got {a}");
    }
}